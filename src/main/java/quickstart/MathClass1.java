package quickstart;


public class MathClass1 {


    public int Add(int Num1,int Num2)
    {
        return Num1+Num2;
    }

    public long Multiply(int Num1,int Num2)
    {
        return Num1*Num2;
    }

    public float Divide(int Num1,int Num2)
    {
        return Num1/Num2;
    }

    public int subtract(int Num1,int Num2)
    {
        return Num1-Num2;
    }

}
